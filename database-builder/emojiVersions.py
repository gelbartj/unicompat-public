for ver, sequenceGroup in vers.items():
    seqlen = len(sequenceGroup)
    for idx, sequenceItem in enumerate(sequenceGroup):
        cpList = [ord(a) for a in list(sequenceItem.split(" ")[0])]
        if len(cpList) == 1:
            print("Found glyph, not sequence. Skipping ",
                  sequenceItem.split(" ")[0])
            continue
        #print("Sequence is ", sequenceItem.split(" ")[0])
        try:
            seq = Sequence.objects.get(cpList=cpList)
            print(f"Got seq obj {idx + 1}/{seqlen}", end='\r')
            if seq.minUnicodeVersion is None:
                print("~~~~Missing univer in ", seq)
                seq.minUnicodeVersion = UnicodeVersion.objects.get(number=ver)
                seq.save()
        except Sequence.DoesNotExist:
            print("Couldn't find ", seq)

